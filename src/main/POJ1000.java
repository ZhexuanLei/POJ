package main;

import java.util.Scanner;

//http://poj.org/problem?id=1000

public class POJ1000 {

	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		int a = input.nextInt();
		int b = input.nextInt();
		int sum = a + b;
		System.out.println(sum);
		
		input.close();
	}

}
